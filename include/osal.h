/**
 * @file    osal.h
 * @brief   操作系统适配层公开接口
 *
 * 提供临界区、信号量、内存分配、时间戳等 OS 无关抽象。
 * 移植到不同平台时只需实现本模块。
 */

#ifndef OSAL_H
#define OSAL_H

#include <stdint.h>
#include <stddef.h>

/* -------------------------------------------------------------------------- */
/*  类型定义                                                                  */
/* -------------------------------------------------------------------------- */

/** 临界区保护句柄（不透明） */
typedef void *osal_critical_t;

/** 信号量句柄（不透明） */
typedef void *osal_sem_t;

/** 互斥锁句柄（不透明） */
typedef void *osal_mutex_t;

/* -------------------------------------------------------------------------- */
/*  临界区                                                                    */
/* -------------------------------------------------------------------------- */

/**
 * @brief 进入临界区（禁止中断或获取锁）。
 * @return 临界区句柄，用于退出时传入。
 */
osal_critical_t osal_critical_enter(void);

/**
 * @brief 退出临界区（恢复中断或释放锁）。
 * @param crit 进入时返回的句柄。
 */
void osal_critical_exit(osal_critical_t crit);

/* -------------------------------------------------------------------------- */
/*  信号量（用于异步通知）                                                     */
/* -------------------------------------------------------------------------- */

/**
 * @brief 创建一个信号量（初始计数为 0）。
 * @param max_count 最大计数。
 * @return 信号量句柄，失败返回 NULL。
 */
osal_sem_t osal_sem_create(uint32_t max_count);

/**
 * @brief 删除信号量。
 * @param sem 信号量句柄。
 */
void osal_sem_delete(osal_sem_t sem);

/**
 * @brief 等待信号量（阻塞直到计数 > 0）。
 * @param sem     信号量句柄。
 * @param timeout 超时时间（毫秒），0 表示永久等待。
 * @return 0 成功获取，-1 超时。
 */
int osal_sem_wait(osal_sem_t sem, uint32_t timeout);

/**
 * @brief 发送信号量（计数 +1，唤醒一个等待者）。
 * @param sem 信号量句柄。
 */
void osal_sem_signal(osal_sem_t sem);

/* -------------------------------------------------------------------------- */
/*  互斥锁                                                                    */
/* -------------------------------------------------------------------------- */

/**
 * @brief 创建一个互斥锁。
 * @return 互斥锁句柄，失败返回 NULL。
 */
osal_mutex_t osal_mutex_create(void);

/**
 * @brief 删除互斥锁。
 * @param mutex 互斥锁句柄。
 */
void osal_mutex_delete(osal_mutex_t mutex);

/**
 * @brief 上锁。
 * @param mutex 互斥锁句柄。
 */
void osal_mutex_lock(osal_mutex_t mutex);

/**
 * @brief 解锁。
 * @param mutex 互斥锁句柄。
 */
void osal_mutex_unlock(osal_mutex_t mutex);

/* -------------------------------------------------------------------------- */
/*  内存管理                                                                  */
/* -------------------------------------------------------------------------- */

/**
 * @brief 分配内存（协议栈内部使用，替代 malloc）。
 * @param size 分配大小（字节）。
 * @return 内存指针，失败返回 NULL。
 */
void *osal_malloc(size_t size);

/**
 * @brief 释放内存。
 * @param ptr 内存指针。
 */
void osal_free(void *ptr);

/* -------------------------------------------------------------------------- */
/*  时间戳                                                                    */
/* -------------------------------------------------------------------------- */

/**
 * @brief 获取系统启动以来的毫秒时间戳（用于定时器基准）。
 * @return 当前毫秒数。
 */
uint32_t osal_get_time_ms(void);

/* -------------------------------------------------------------------------- */
/*  初始化                                                                    */
/* -------------------------------------------------------------------------- */

/**
 * @brief 初始化 OSAL 模块（在协议栈启动前调用）。
 */
void osal_init(void);

#endif /* OSAL_H */